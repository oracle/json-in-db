package movie.model;

public class Image {

    String description;
    
    String file;
    
    public Image () {
        
    }
    
    public Image(String file, String description) {
        this.file = file;
        this.file = file;
    }

    public String getFile() {
        return file;
    }

    public void setFile(String file) {
        this.file = file;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }
    
    
}
